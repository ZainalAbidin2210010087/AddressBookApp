    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
    String name = nameField.getText();
    String address = addressField.getText();
    String phone = phoneField.getText();

    if (!name.isEmpty() && !address.isEmpty() && !phone.isEmpty()) {
        Contact contact = new Contact(name, address, phone);
        contactMap.put(name, contact);     // Tambahkan ke contactMap
        listModel.addElement(name);        // Tambahkan ke listModel sebagai nama saja
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, "Mohon isi semua field");
    }
    }//GEN-LAST:event_addButtonActionPerformed

