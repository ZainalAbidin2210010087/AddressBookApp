    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
    String selectedName = contactList.getSelectedValue();
    if (selectedName != null) {
        listModel.removeElement(selectedName); // Hapus dari listModel
        contactMap.remove(selectedName);       // Hapus dari contactMap
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, "Pilih kontak untuk dihapus");
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void contactListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_contactListMouseClicked
       String selectedName = contactList.getSelectedValue();
    if (selectedName != null && contactMap.containsKey(selectedName)) {
        Contact contact = contactMap.get(selectedName);
        nameField.setText(contact.getName());
        addressField.setText(contact.getAddress());
        phoneField.setText(contact.getPhoneNumber());
    }
    }//GEN-LAST:event_contactListMouseClicked
private void clearFields() {
    nameField.setText("");
    addressField.setText("");
    phoneField.setText("");
}

